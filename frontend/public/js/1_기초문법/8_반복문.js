// 예제2) i는 1부터 10이하까지 1씩 증가 반복 [ 한줄에 표시]
console.log('---------------ex2----------------');
let output= '' // 문자열을 누적으로 저장할 변수 선언
for(let i = 0; i <= 10; i++) {
	output+= `${i} ` //	\t : 제어문자 (들여쓰기)
} // f end
console.log(output)

