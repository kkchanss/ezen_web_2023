// 예제4) i는 1부터 10이하까지 1씩 증가 반복 ( 누적 합계)
console.log('---------------ex4----------------');
let sum = 0;
for(let i = 1; i <= 10; i++) {
	
	// 1. sum = sum + i
	// 2. sum += i
	sum += i; // 기존 sum 변수에 i를 더한값을 sum 변수에 대입
} // f end
console.log(sum)
/* 
	
	초기값 i = 1 / sum = 0;
	for 시작
		i		조건			실행		누계			증감
		i = 1	1<=10 T		sum+=1	0+1=>1		i++
		i = 2	2<=10 T		sum+=2	1+2=>3		i++
		i = 3	3<=10 T		sum+=3	3+3=>6		i++
		i = 4	4<=10 T		sum+=4	6+4=>10		i++
		i = 5	5<=10 T		sum+=5	10+5=>15	i++
*/

